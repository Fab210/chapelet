// Extrait le texte brut depuis le HTML (supprime les balises)
function extraireTexte(html) {
  const tmp = document.createElement("div");
  tmp.innerHTML = html;
  return tmp.textContent || tmp.innerText || "";
}

// Version nettoyée pour le TTS : supprime les références bibliques et compteurs
function extraireTexteTTS(html) {
  const tmp = document.createElement("div");
  tmp.innerHTML = html;
  let texte = tmp.textContent || tmp.innerText || "";
  // Supprime les références bibliques : (Lc 1,26-35), (Mt 3,13-17.46), (Ap 12,1)…
  texte = texte.replace(/\([A-Za-zÀ-ÿ]+\s+\d+[\d,.\s\-–]*\)/g, "");
  // Supprime le compteur Ave Maria : (3/10)
  texte = texte.replace(/\s*\(\d+\/10\)/g, "");
  return texte.replace(/\s+/g, " ").trim();
}

