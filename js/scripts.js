// --- GÉNÉRATION DES PERLES (responsive) ---
function generatePerles() {
  const extremiteContainer = document.getElementById("extremite");
  const cercleContainer = document.getElementById("cercle");

  // Taille réelle du chapelet au moment du rendu.
  // DOIT rester synchronisé avec --chap-size dans styles.css (sinon les
  // perles, positionnées en JS, se décalent du conteneur dimensionné en CSS).
  let chapSize;
  if (window.innerWidth >= 1025) {
    // Desktop « livre » deux colonnes : chapelet dans la colonne droite
    chapSize = Math.min(500, window.innerWidth * 0.9, (window.innerHeight - 260) / 1.45);
  } else if (window.innerWidth >= 768) {
    // Tablette en colonne unique : plus de place, on agrandit
    chapSize = Math.min(600, window.innerWidth * 0.85, (window.innerHeight - 320) / 1.45);
  } else {
    chapSize = Math.min(500, window.innerWidth * 0.9);
  }
  const scale = chapSize / 500; // facteur par rapport à la base 500px

  // Perles de la chaîne verticale — positions proportionnelles
  const perlesVerticales = [
    { class: "croix",            top: Math.round(165 * scale) },
    { class: "perle notre-pere", top: Math.round(130 * scale) },
    { class: "perle",            top: Math.round(100 * scale) },
    { class: "perle",            top: Math.round( 70 * scale) },
    { class: "perle",            top: Math.round( 40 * scale) },
  ];

  perlesVerticales.forEach((data) => {
    const perle = document.createElement("div");
    perle.className = data.class;
    perle.style.position = "absolute";
    perle.style.top = `${data.top}px`;
    perle.style.left = "50%";
    perle.style.transform = "translateX(-50%)";
    extremiteContainer.appendChild(perle);
  });

  // Perles circulaires — rayon proportionnel
  const totalPerles = 60;
  const radius = 250 * scale;
  const center = 250 * scale;
  const startAngle = Math.PI / 2;
  const angleIncrement = (2 * Math.PI) / (totalPerles - 5);

  for (let i = 5; i < totalPerles; i++) {
    const angle = startAngle - (i - 5) * angleIncrement;
    const x = Math.cos(angle) * radius + center;
    const y = Math.sin(angle) * radius + center;

    const perle = document.createElement("div");
    // Taille des perles déjà gérée en CSS via calc(min(500px,90vw)*0.036)
    // On positionne uniquement avec le centre
    const sizeBase = [5, 16, 27, 38, 49].includes(i) ? 22 : 18;
    const size = sizeBase * scale;
    perle.className = [5, 16, 27, 38, 49].includes(i)
      ? "perle notre-pere"
      : "perle";
    perle.style.position = "absolute";
    perle.style.left = `${x - size / 2}px`;
    perle.style.top = `${y - size / 2}px`;
    cercleContainer.appendChild(perle);
  }
}

// --- COMPTER LES AVE MARIA ---
function compterAveMaria(index) {
  let count = 0;
  for (let i = index; i >= 0; i--) {
    if (prières[i] === aveMaria) count++;
    else if (prières[i] === paiNosso) break;
  }
  return count;
}

// Reste-t-il une page après la position courante ?
// (perle suivante, ou pages intercalées — y compris la clôture à l'index 60)
function aProchainePage() {
  const next = currentIndex + 1;
  if (sousTextes[next]) return true;
  return next <= prières.length - 1;
}

// --- AVANCER / RECULER ---
function avancePriere(depuisTTS = false) {
  const nextIndex = currentIndex + 1;

  if (sousTextes[nextIndex]) {
    if (!sousIndexes[nextIndex]) sousIndexes[nextIndex] = 0;
    const currentSousIndex = sousIndexes[nextIndex];
    currentPriere.innerHTML = sousTextes[nextIndex][currentSousIndex];
    history.push({
      type: "sous",
      index: nextIndex,
      subIndex: currentSousIndex,
    });

    if (currentSousIndex === 0) {
      const numMystere = Object.keys(sousTextes).indexOf(String(nextIndex));
      changerImageMystere(numMystere);
    }

    sousIndexes[nextIndex]++;
    if (sousIndexes[nextIndex] >= sousTextes[nextIndex].length) {
      sousIndexes[nextIndex] = 0;
      currentIndex = nextIndex;
    }
  } else {
    currentIndex = nextIndex;
    let texte = prières[currentIndex] || "";
    if (prières[currentIndex] === aveMaria) {
      const count = compterAveMaria(currentIndex);
      // 3 Ave Maria d'ouverture (index < 5) vs dizaines de 10
      const total = currentIndex < 5 ? 3 : 10;
      texte = texte.replace(/(<h1>)(.*?)(<\/h1>)/, `$1$2 (${count}/${total})$3`);
    }
    currentPriere.innerHTML = texte;
    history.push({ type: "priere", index: currentIndex });
  }

  updateRosaire();
  if (!depuisTTS) tts.stopper();
  fermerModal();
  document.getElementById("avance").disabled = !aProchainePage();
}

function reculePriere() {
  if (!history.length) return;
  history.pop();

  for (let k in sousIndexes) delete sousIndexes[k];

  let lastPriereIndex = -1;
  history.forEach((h) => {
    if (h.type === "sous")
      sousIndexes[h.index] = Math.max(
        sousIndexes[h.index] || 0,
        h.subIndex + 1
      );
    else if (h.type === "priere") lastPriereIndex = h.index;
  });

  const last = history[history.length - 1];
  if (!last) {
    currentIndex = -1;
    currentPriere.innerHTML = buildPageInitiale(typeMystere);
  } else if (last.type === "priere") {
    currentIndex = last.index;
    currentPriere.innerHTML = prières[currentIndex];
  } else if (last.type === "sous") {
    currentPriere.innerHTML = sousTextes[last.index][last.subIndex];
    currentIndex = last.index - 1;
  }

  updateRosaire();
  // Stopper la lecture audio
  tts.stopper();
  // Fermer le modal si ouvert (prière précédente)
  fermerModal();
  document.getElementById("avance").disabled = !aProchainePage();
}

// --- MISE À JOUR DES PERLES ET IMAGE ---
function updateRosaire() {
  // État des boutons de navigation (recalculé à chaque mise à jour, y compris
  // après un changement de mystère/langue — sinon « Avancer » resterait
  // désactivé une fois le chapelet terminé et on ne pourrait pas recommencer).
  const recule = document.getElementById("recule");
  if (recule) recule.disabled = history.length === 0;
  const avance = document.getElementById("avance");
  if (avance) avance.disabled = !aProchainePage();

  perlesAtteintes.length = 0;
  history.forEach((h) => {
    if (!perlesAtteintes.includes(h.index)) perlesAtteintes.push(h.index);
  });
  mettreAJourPerles();
  mettreAJourBandeau();

  let dernierMystereIndex = -1;
  for (let i = history.length - 1; i >= 0; i--) {
    const h = history[i];
    if (h.type === "sous") {
      dernierMystereIndex = Object.keys(sousTextes).indexOf(String(h.index));
      break;
    }
  }

  if (dernierMystereIndex >= 0) {
    changerImageMystere(dernierMystereIndex);
  }
}

