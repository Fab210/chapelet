// Contrôle de vitesse TTS
const speedSteps  = [0.60, 0.75, 0.88, 1.0, 1.15];
const speedLabels = ["×0.6", "×0.75", "×0.9", "×1.0", "×1.1"];
let speedIndex = 2; // 0.88× par défaut

function updateSpeedDisplay() {
  const el      = document.getElementById("speed-display");
  const btnDown = document.getElementById("btn-speed-down");
  const btnUp   = document.getElementById("btn-speed-up");
  if (el)      el.textContent  = speedLabels[speedIndex];
  if (btnDown) btnDown.disabled = speedIndex === 0;
  if (btnUp)   btnUp.disabled   = speedIndex === speedSteps.length - 1;
}

const btnSpeedDown = document.getElementById("btn-speed-down");
const btnSpeedUp   = document.getElementById("btn-speed-up");

if (btnSpeedDown) {
  btnSpeedDown.addEventListener("click", () => {
    if (speedIndex > 0) {
      speedIndex--;
      tts.vitesse = speedSteps[speedIndex];
      updateSpeedDisplay();
      tts.changerVitesse();
    }
  });
}

if (btnSpeedUp) {
  btnSpeedUp.addEventListener("click", () => {
    if (speedIndex < speedSteps.length - 1) {
      speedIndex++;
      tts.vitesse = speedSteps[speedIndex];
      updateSpeedDisplay();
      tts.changerVitesse();
    }
  });
}

updateSpeedDisplay();

// Hint de navigation : swipe (tactile) ou clavier (desktop). Le CSS n'en
// affiche qu'un seul selon l'appareil ; on anime l'opacité des deux.
["swipe-hint", "clavier-hint"].forEach((id) => {
  const hint = document.getElementById(id);
  if (!hint) return;
  setTimeout(() => { hint.style.opacity = "1"; }, 600);
  setTimeout(() => { hint.style.opacity = "0"; }, 4200);
});
