// LECTURE AUDIO TTS (Web Speech API)
// ============================================================

const tts = {
  actif: false,
  autoPlay: false,
  _enPause: false,
  voix: null,
  vitesse: 0.88,
  pitch: 0.85,
  volume: 1,
  _texteActuel: "",
  _charIndex: 0,
  _seq: 0,
  _autoErreurs: 0,

  changerVoix(langCodes) {
    if (!window.speechSynthesis) return;
    const voix = speechSynthesis.getVoices();
    if (!voix.length) return;

    const NOMS_MASCULINS = /\b(male|man|david|daniel|guy|davis|stefan|paulo|diego|carlos|jorge|mark|james|tom|alex|henri|thomas|pierre|nicolas|felix|oliver|george|ryan|reed|andrew|luca|pablo|miguel|antonio)\b/;
    const isMale = (v) => {
      const n = v.name.toLowerCase();
      return NOMS_MASCULINS.test(n);
    };
    const score = (v) => {
      const n = v.name.toLowerCase();
      let s = 0;
      if (isMale(v))                                          s += 8;
      if (n.includes("natural") || n.includes("neural"))     s += 4;
      if (n.includes("google") || n.includes("microsoft"))   s += 2;
      return s;
    };

    for (const code of langCodes) {
      const matches = voix.filter(v => v.lang === code);
      if (matches.length) {
        tts.voix = matches.sort((a, b) => score(b) - score(a))[0];
        return;
      }
    }
    for (const code of langCodes) {
      const matches = voix.filter(v => v.lang.startsWith(code.split("-")[0]));
      if (matches.length) {
        tts.voix = matches.sort((a, b) => score(b) - score(a))[0];
        return;
      }
    }
    tts.voix = null;
  },

  // Attendre que les voix soient prêtes, puis choisir
  init() {
    if (!window.speechSynthesis) return;

    const choisirVoix = () => {
      const t = traductions[langueActuelle];
      tts.changerVoix(t.ttsLang);
      console.log("[TTS] Voix choisie :", tts.voix ? `${tts.voix.name} (${tts.voix.lang})` : "défaut système");
    };

    // Certains navigateurs (Chrome, Edge) chargent les voix de façon asynchrone
    if (speechSynthesis.getVoices().length) {
      choisirVoix();
    }
    speechSynthesis.addEventListener("voiceschanged", choisirVoix);
  },

  lire(texte) {
    if (!window.speechSynthesis || !texte.trim()) return;
    if (speechSynthesis.paused) speechSynthesis.resume();
    speechSynthesis.cancel();
    tts._texteActuel = texte;
    tts._charIndex = 0;
    tts._seq++;
    tts._lireDepuis(0);
  },

  _lireDepuis(offset) {
    const extrait = tts._texteActuel.slice(offset);
    if (!extrait.trim()) return;

    const utterance = new SpeechSynthesisUtterance(extrait);
    utterance.lang   = tts.voix ? tts.voix.lang : traductions[langueActuelle].ttsLang[0];
    if (tts.voix) utterance.voice = tts.voix;
    utterance.rate   = tts.vitesse;
    utterance.pitch  = tts.pitch;
    utterance.volume = tts.volume;

    // Enchaînement automatique vers la prière suivante.
    // Appelé sur onend (succès) ET sur onerror non volontaire, pour qu'une voix
    // qui échoue (ex. voix réseau d'une autre langue) ne fige pas toute la
    // récitation après la première prière.
    const continuerAutoPlay = (estErreur) => {
      tts.actif = false;
      tts._mettreAJourBouton();
      musique.restaurerVolume();
      if (!tts.autoPlay) return;
      // Garde-fou : si plusieurs prières échouent d'affilée (voix de la langue
      // réellement indisponible), on arrête au lieu de défiler en silence.
      if (estErreur) {
        if (++tts._autoErreurs > 2) { tts.autoPlay = false; tts._autoErreurs = 0; return; }
      } else {
        tts._autoErreurs = 0;
      }
      const btnAvance = document.getElementById("avance");
      if (!btnAvance || btnAvance.disabled) {
        tts.autoPlay = false;
        return;
      }
      avancePriere(true);
      // Lecture de la prière suivante SANS speechSynthesis.cancel() : sur Safari
      // (macOS), un cancel() suivi de speak() hors d'un geste utilisateur échoue
      // souvent en silence — cause probable de l'arrêt après la 1re prière.
      // Ici le moteur est déjà au repos (utterance terminée), cancel() est inutile.
      setTimeout(() => {
        if (!tts.autoPlay) return;
        const texteSuivant = extraireTexteTTS(currentPriere.innerHTML);
        if (!texteSuivant.trim()) return;
        tts._texteActuel = texteSuivant;
        tts._charIndex = 0;
        tts._seq++;
        tts._lireDepuis(0);
      }, 600);
    };

    utterance.onboundary = (e) => { tts._charIndex = offset + e.charIndex; };
    utterance.onstart    = () => { tts.actif = true;  tts._mettreAJourBouton(); musique.ducker(); };
    utterance.onend      = () => continuerAutoPlay(false);
    utterance.onerror    = (e) => {
      // « interrupted »/« canceled » = annulation volontaire (notre cancel(),
      // changement de prière/langue) : ne pas enchaîner, sinon double avance.
      const err = e && e.error;
      if (err === "interrupted" || err === "canceled") {
        tts.actif = false;
        tts._mettreAJourBouton();
        musique.restaurerVolume();
        return;
      }
      // Vraie erreur de synthèse : on poursuit la prière suivante.
      continuerAutoPlay(true);
    };

    speechSynthesis.speak(utterance);
  },

  changerVitesse() {
    if (!window.speechSynthesis) return;
    if (tts.actif || speechSynthesis.paused) {
      const pos = tts._charIndex;
      const seq = ++tts._seq;
      if (speechSynthesis.paused) speechSynthesis.resume();
      speechSynthesis.cancel();
      setTimeout(() => { if (tts._seq === seq) tts._lireDepuis(pos); }, 80);
    }
  },

  lirePriereCourante() {
    const texte = extraireTexteTTS(currentPriere.innerHTML);
    tts.lire(texte);
  },

  togglePause() {
    if (!window.speechSynthesis) return;
    if (tts.actif) {
      speechSynthesis.pause();
      tts.actif = false;
      tts._enPause = true;
      musique.restaurerVolume();
    } else if (tts._enPause) {
      speechSynthesis.resume();
      tts.actif = true;
      tts._enPause = false;
      musique.ducker();
    } else {
      speechSynthesis.cancel();
      tts.autoPlay = true;
      tts.lirePriereCourante();
    }
    tts._mettreAJourBouton();
  },

  stopper() {
    if (!window.speechSynthesis) return;
    if (speechSynthesis.paused) speechSynthesis.resume();
    speechSynthesis.cancel();
    tts.actif = false;
    tts.autoPlay = false;
    tts._enPause = false;
    tts._autoErreurs = 0;
    tts._mettreAJourBouton();
    musique.restaurerVolume();
  },

  _mettreAJourBouton() {
    const btn = document.getElementById("btn-tts");
    if (!btn) return;
    const ui = traductions[langueActuelle].ui;
    const enLecture = !!window.speechSynthesis && speechSynthesis.speaking && !speechSynthesis.paused;
    const iconEl = btn.querySelector("#tts-icon");
    if (iconEl) iconEl.textContent = enLecture ? "⏸" : "🔊";
    btn.setAttribute("aria-label", enLecture ? ui.pause : ui.lire);
  },
};

