(function initSwipe() {
  let touchStartX = 0;
  let touchStartY = 0;
  const SWIPE_MIN = 50;   // px minimum pour déclencher
  const SWIPE_MAX_Y = 80; // px max vertical (évite les scrolls)

  document.addEventListener("touchstart", (e) => {
    touchStartX = e.changedTouches[0].clientX;
    touchStartY = e.changedTouches[0].clientY;
  }, { passive: true });

  document.addEventListener("touchend", (e) => {
    const dx = e.changedTouches[0].clientX - touchStartX;
    const dy = Math.abs(e.changedTouches[0].clientY - touchStartY);

    // Vérifier que c'est un swipe horizontal et non un scroll vertical
    if (dy > SWIPE_MAX_Y) return;

    if (dx < -SWIPE_MIN) {
      // Swipe vers la gauche → avancer
      if (!document.getElementById("avance").disabled) {
        flashBouton("avance");
        avancePriere();
      }
    } else if (dx > SWIPE_MIN) {
      // Swipe vers la droite → reculer
      flashBouton("recule");
      reculePriere();
    }
  }, { passive: true });
})();

